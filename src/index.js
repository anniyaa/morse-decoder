const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let strPer10 = expr.match(/.{10}/g);
    let result ='';

    for (string in strPer10) {
        MORSE_TABLE['**********'] = ' ';
        for (key in MORSE_TABLE) {
            if (strPer10[string] === key.replace(/-/g, '11').replace(/\./g, '10').padStart(10, '0')) {
                result = result + MORSE_TABLE[key];
            }
        }
    }

    return result;
}

module.exports = {
    decode
}